# Lambda Functions

# Its like the Arrow Function in Javascript

# Lambda is a anonymous function

# ANONYMOUS LIST :

[1,2,3,4,5,6,7,8,9]

def multiplicar_por_dos(x):
    return x*2

print(multiplicar_por_dos(2))#4

#instead

multiplicar_porr_Dos = lambda x : x*2

print(multiplicar_porr_Dos(2)) #4

