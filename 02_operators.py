# Operadores

# hay muchos tipos de operadores : aritmeticos, logicos,  acceso, comparacion, incremento, de asignacion


# Aritmeticos 

print(5 + 7 )

# Logicos

print(True & True )


# acceso

lista = ["hola","chau"]

print(lista[0])

# Comparacion

print("hola" == "hola")

#Incremento

